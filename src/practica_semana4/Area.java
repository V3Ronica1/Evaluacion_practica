
package practica_semana4;


public class Area {
    int codigo_area;
    String nombre;
    int cantidad_empleados,num_edificio;

    public int getCodigo_area() {
        return codigo_area;
    }

    public void setCodigo_area(int codigo_area) {
        this.codigo_area = codigo_area;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCantidad_empleados() {
        return cantidad_empleados;
    }

    public void setCantidad_empleados(int cantidad_empleados) {
        this.cantidad_empleados = cantidad_empleados;
    }

    public int getNum_edificio() {
        return num_edificio;
    }

    public void setNum_edificio(int num_edificio) {
        this.num_edificio = num_edificio;
    }
    
}
